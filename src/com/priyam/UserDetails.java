package com.priyam;

public class UserDetails {
	private String email;
	private String about;
	private String prof_pic_path;

	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getAbout() {
		return about;
	}
	public void setAbout(String about) {
		this.about = about;
	}
	public String getProf_pic_path() {
		return prof_pic_path;
	}
	public void setProf_pic_path(String prof_pic_path) {
		this.prof_pic_path = prof_pic_path;
	}
	
}
